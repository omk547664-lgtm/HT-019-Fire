const express = require('express');
const router = express.Router();
const Product = require('../models/Product');
// const auth = require('../middleware/auth'); // TODO: Add auth middleware later

// Get all products
router.get('/', async (req, res) => {
    try {
        const products = await Product.find();
        res.json(products);
    } catch (err) {
        res.status(500).json({ message: err.message });
    }
});

// Add product
router.post('/', async (req, res) => {
    const { name, category, costPrice, sellingPrice, stock, reorderLevel, supplier } = req.body;
    try {
        const newProduct = new Product({
            name, category, costPrice, sellingPrice, stock, reorderLevel, supplier
        });
        const savedProduct = await newProduct.save();
        res.status(201).json(savedProduct);
    } catch (err) {
        res.status(400).json({ message: err.message });
    }
});

// Update product
router.put('/:id', async (req, res) => {
    try {
        const updatedProduct = await Product.findByIdAndUpdate(req.params.id, req.body, { new: true });
        res.json(updatedProduct);
    } catch (err) {
        res.status(400).json({ message: err.message });
    }
});

// Delete product
router.delete('/:id', async (req, res) => {
    try {
        await Product.findByIdAndDelete(req.params.id);
        res.json({ message: 'Product deleted' });
    } catch (err) {
        res.status(500).json({ message: err.message });
    }
});

module.exports = router;
